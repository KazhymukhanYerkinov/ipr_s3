/// Composite pattern — базовый интерфейс для файлов и папок.
///
/// Позволяет единообразно работать с отдельными файлами и папками
/// (которые содержат файлы и другие папки). Клиентский код вызывает
/// [totalSize] или [totalFiles] и получает корректный результат
/// независимо от глубины вложенности.
abstract class FileSystemItem {
  String get id;
  String get name;

  /// Суммарный размер всех вложенных элементов (в байтах).
  int get totalSize;

  /// Суммарное количество файлов (папки не считаются).
  int get totalFiles;
}
